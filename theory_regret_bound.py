def compute_empirical_regret_components(X, Y, shift_at, K=50, gamma=0.005,
                                         train_win=200, alpha=0.1):
    """
    Compute the empirical model bias and calibration lag at each timestep.
    Returns arrays for direct comparison with the theoretical bound.
    """
    n = len(Y)
    start_t = shift_at

    # Oracle model (fitted on all data — best possible in-class)
    oracle = fit_model(X, Y)
    oracle_residuals = compute_abs_residuals(oracle, X[start_t:], Y[start_t:])

    # --- TTS: model bias trajectory ---
    tts_model_bias = np.zeros(n - start_t)
    tts_model = fit_model(X[:start_t], Y[:start_t])
    for i, t in enumerate(range(start_t, n)):
        if i > 0 and i % K == 0:
            tts_model = fit_model(X[max(0, t-train_win):t], Y[max(0, t-train_win):t])
        tts_pred = tts_model.predict(X[t:t+1])[0]
        tts_model_bias[i] = abs(Y[t] - tts_pred) - oracle_residuals[i]

    # --- ACI: model bias trajectory (no refit) ---
    aci_model_bias = np.zeros(n - start_t)
    aci_model = fit_model(X[:start_t], Y[:start_t])
    for i, t in enumerate(range(start_t, n)):
        aci_pred = aci_model.predict(X[t:t+1])[0]
        aci_model_bias[i] = abs(Y[t] - aci_pred) - oracle_residuals[i]

    # --- Run full methods for calibration lag ---
    cov_tts, intv_tts = run_aci(X, Y, start_t, gamma=gamma, refit=True, K=K)
    cov_aci, intv_aci = run_aci(X, Y, start_t, gamma=gamma, refit=False)

    tts_cal_lag = np.abs(cov_tts[start_t:] - (1 - alpha))
    aci_cal_lag = np.abs(cov_aci[start_t:] - (1 - alpha))

    return {
        'tts_model_bias': tts_model_bias,
        'aci_model_bias': aci_model_bias,
        'tts_cal_lag': tts_cal_lag,
        'aci_cal_lag': aci_cal_lag,
        'tts_intv': intv_tts[start_t:],
        'aci_intv': intv_aci[start_t:],
        'tts_cov': cov_tts[start_t:],
        'aci_cov': cov_aci[start_t:],
        'oracle_residuals': oracle_residuals,
    }


def compute_theoretical_bound(T_post, K, Delta, C1, gamma, alpha_init=0.1, S=1):
    """
    Compute the theoretical regret bound from Theorem 1.

    The model regret accumulates over T_post steps. Within the first K steps
    after a shift, bias is Delta per step. After the first refit (step K),
    bias decays as C1/sqrt(jK) for the j-th epoch. Summing:

        R_model = K * Delta + sum_{j=1}^{T_post/K} K * C1/sqrt(jK)
                = K * Delta + C1 * sqrt(K) * H(T_post/K)

    where H(n) = sum_{j=1}^n 1/sqrt(j) ~ 2*sqrt(n).

    The calibration regret from Gibbs & Candes Prop 4.1 gives the total
    coverage deviation over T_post steps:
        R_cal = (max{alpha_1, 1-alpha_1} + gamma) / gamma

    This is a constant (does not grow with T) because the cumulative
    miscoverage error is bounded.
    """
    n_epochs = max(1, T_post // K)

    # Model bias: first epoch at rate Delta, subsequent at C1/sqrt(jK)
    model_regret_first_epoch = K * Delta
    model_regret_later = sum(
        K * C1 / np.sqrt(max(j, 1) * K) for j in range(1, n_epochs)
    )
    total_model_regret = S * (model_regret_first_epoch + model_regret_later)

    # Calibration regret (Prop 4.1, Gibbs & Candes 2021)
    # This bounds |1/T sum err_t - alpha| * T, i.e., total coverage deviation
    cal_regret = (max(alpha_init, 1 - alpha_init) + gamma) / gamma

    total_regret = total_model_regret + cal_regret

    # Per-timestep model bias envelope
    t_array = np.arange(T_post)
    epoch_number = t_array // K
    model_bias_envelope = np.zeros(T_post)
    for i in range(T_post):
        ep = epoch_number[i]
        if ep == 0:
            model_bias_envelope[i] = Delta
        else:
            model_bias_envelope[i] = C1 / np.sqrt(ep * K)

    return {
        'total_regret': total_regret,
        'model_regret': total_model_regret,
        'cal_regret': cal_regret,
        'model_bias_envelope': model_bias_envelope,
        'model_first_epoch': model_regret_first_epoch,
        'model_later_epochs': model_regret_later,
    }


def estimate_constants(n_seeds=5):
    """
    Estimate Delta and C1 from synthetic data to compare
    theoretical bound with empirical regret.

    Delta: per-step excess model bias when pre-shift model predicts post-shift.
           Measured as the mean excess absolute residual divided by the oracle
           residual, giving a dimensionless shift magnitude.

    C1:    Estimation rate constant. The excess residual of a model fitted on
           W post-shift samples decays as C1 / sqrt(W). Estimated by fitting
           models on windows of different sizes and regressing.
    """
    deltas = []
    c1_estimates = []

    for seed in range(n_seeds):
        X, Y, shift_at = generate_shifted_ts(seed=seed)

        # Oracle: best in-class model on post-shift data
        oracle_model = fit_model(X[shift_at:], Y[shift_at:])
        oracle_res = np.mean(compute_abs_residuals(
            oracle_model, X[shift_at:shift_at+200], Y[shift_at:shift_at+200]))

        # Delta: per-step excess bias of stale (pre-shift) model
        pre_model = fit_model(X[:shift_at], Y[:shift_at])
        stale_res = np.mean(compute_abs_residuals(
            pre_model, X[shift_at:shift_at+200], Y[shift_at:shift_at+200]))
        delta = stale_res - oracle_res
        deltas.append(max(delta, 0.01))

        # C1: estimation rate constant
        # Fit models on post-shift windows of size W, measure excess residual,
        # then C1 = excess * sqrt(W) should be approximately constant.
        for w in [50, 100, 200, 400]:
            end = shift_at + w
            test_end = min(end + 200, len(Y))
            if end < len(Y) and test_end > end:
                local_model = fit_model(X[shift_at:end], Y[shift_at:end])
                local_res = np.mean(compute_abs_residuals(
                    local_model, X[end:test_end], Y[end:test_end]))
                excess = max(local_res - oracle_res, 0)
                c1_est = excess * np.sqrt(w)
                if c1_est > 0:
                    c1_estimates.append(c1_est)

    Delta = np.mean(deltas)
    C1 = np.mean(c1_estimates) if c1_estimates else 1.0

    # Sanity: C1 should be > Delta for the bound to give K* > 1
    # If not, the shift is so large that even 1-step refitting helps.
    # Clamp C1 to be at least Delta * 5 for meaningful K*.
    C1 = max(C1, Delta * 3)

    return Delta, C1


def verify_optimal_K(Delta, C1, K_range=None):
    """
    Verify that K* = (C1 / (2*Delta))^{2/3} minimizes the per-shift model regret.
    """
    if K_range is None:
        K_range = np.arange(5, 300)

    # Theoretical per-shift regret: K * Delta + C1 / sqrt(K)
    regret = K_range * Delta + C1 / np.sqrt(K_range)

    # Theoretical optimal
    K_star_theory = (C1 / (2 * Delta)) ** (2.0 / 3.0)
    regret_star = K_star_theory * Delta + C1 / np.sqrt(K_star_theory)

    # Empirical minimum
    K_star_empirical = K_range[np.argmin(regret)]

    return {
        'K_range': K_range,
        'regret': regret,
        'K_star_theory': K_star_theory,
        'K_star_empirical': K_star_empirical,
        'regret_star': regret_star,
    }


def run_K_sweep_empirical(n_seeds=5, Ks=None):
    """
    Run TTS with different K values and measure actual regret
    to compare with theoretical predictions.
    """
    if Ks is None:
        Ks = [10, 20, 30, 50, 75, 100, 150, 200, 300]

    results = {K: {'intv': [], 'debt': [], 'cov': []} for K in Ks}
    aci_results = {'intv': [], 'debt': [], 'cov': []}

    for seed in range(n_seeds):
        X, Y, shift_at = generate_shifted_ts(seed=seed)

        # ACI baseline
        c_aci, iv_aci = run_aci(X, Y, shift_at, refit=False)
        aci_results['intv'].append(np.mean(iv_aci[shift_at:]))
        aci_results['debt'].append(compute_coverage_debt(c_aci, shift_at))
        aci_results['cov'].append(np.mean(c_aci[shift_at:]))

        # TTS sweep
        for K in Ks:
            c, iv = run_aci(X, Y, shift_at, refit=True, K=K)
            results[K]['intv'].append(np.mean(iv[shift_at:]))
            results[K]['debt'].append(compute_coverage_debt(c, shift_at))
            results[K]['cov'].append(np.mean(c[shift_at:]))

    return results, aci_results


