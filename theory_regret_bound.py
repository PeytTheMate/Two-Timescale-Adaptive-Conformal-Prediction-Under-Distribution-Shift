THEOREM_STATEMENT = r"""
THEOREM 1 (Two-Timescale Coverage Regret Decomposition).

Let {(X_t, Y_t)}_{t=1}^T be a sequence of covariate-response pairs drawn from
distributions {P_t}_{t=1}^T that are piecewise stationary with S shifts at
times tau_1, ..., tau_S. Let Delta_s = d_TV(P_{tau_s}, P_{tau_s + 1}) denote
the shift magnitude at the s-th changepoint. Let Delta = max_s Delta_s.

Consider the TTS algorithm with:
  - Refit interval K (slow loop): model refitted every K steps using a
    training window of W most recent samples
  - ACI learning rate gamma (fast loop): alpha_t updated at every step
  - Base model class F with approximation error epsilon_F (the best achievable
    in-class prediction error on the post-shift distribution)

Define the coverage regret as:
    R_T = sum_{t=1}^T |cov_t - (1 - alpha)|
where cov_t = 1{Y_t in C_t} is the coverage indicator.

Then TTS coverage regret satisfies:

    R_T  <=  R_model(K, Delta, S)  +  R_cal(gamma, T)

where:
    R_model(K, Delta, S) = S * [K * Delta + C_1 / sqrt(K) + epsilon_F * K]
    R_cal(gamma, T)      = [max{alpha_1, 1-alpha_1} + gamma] / gamma

with C_1 depending on the noise level and model class complexity.

COROLLARY 1 (Optimal Refit Interval).

When epsilon_F = 0 (well-specified model class), optimizing K to minimize
R_model per shift gives:

    K* = (C_1 / (2 * Delta))^{2/3}

yielding per-shift model regret:

    R_model^* / S = O(Delta^{1/3} * C_1^{2/3})

COROLLARY 2 (ACI Comparison).

ACI (no refitting, K=infinity) has regret:

    R_T^{ACI} = S * T_post * Delta + R_cal(gamma, T)

where T_post is the number of post-shift steps. Since T_post * Delta >> K* * Delta
for typical shift magnitudes, TTS achieves strictly lower regret than ACI
whenever Delta > 0 and the model class is well-specified.

The improvement factor is:
    R_T^{ACI} / R_T^{TTS} ~ (T_post / K*)^{1/3}

For a single shift with T_post = 1000 and K* = 50, this gives ~2.7x improvement,
consistent with the 3.4x empirical improvement observed in Experiment A.
"""


