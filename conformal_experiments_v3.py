def run_static_cp(X, Y, start_t, alpha=0.1, cal_window=200):
    n = len(Y)
    model = fit_model(X[:start_t], Y[:start_t])
    cov, intv = np.zeros(n), np.zeros(n)
    buf = list(compute_abs_residuals(model, X[max(0,start_t-cal_window):start_t],
                                     Y[max(0,start_t-cal_window):start_t]))
    for t in range(start_t, n):
        pred = model.predict(X[t:t+1])[0]
        q = np.quantile(buf[-cal_window:], 1-alpha)
        lo, hi = pred-q, pred+q
        cov[t] = float(lo <= Y[t] <= hi); intv[t] = hi - lo
        buf.append(abs(Y[t] - pred))
    return cov, intv

def run_refit_only(X, Y, start_t, alpha=0.1, K=50, train_win=200, cal_size=50):
    n = len(Y)
    model = fit_model(X[:start_t], Y[:start_t])
    cov, intv = np.zeros(n), np.zeros(n)
    for t in range(start_t, n):
        if (t - start_t) % K == 0 and t > start_t:
            model = fit_model(X[max(0,t-train_win):t], Y[max(0,t-train_win):t])
        pred = model.predict(X[t:t+1])[0]
        cs = max(start_t, t - cal_size)
        res = compute_abs_residuals(model, X[cs:t], Y[cs:t]) if cs < t else np.array([1.0])
        q = np.quantile(res, 1-alpha)
        lo, hi = pred-q, pred+q
        cov[t] = float(lo <= Y[t] <= hi); intv[t] = hi - lo
    return cov, intv

def run_aci(X, Y, start_t, alpha=0.1, gamma=None, cal_window=200,
            refit=False, K=None, train_win=200):
    if gamma is None: gamma = CONFIG['aci_gamma']
    if K is None: K = CONFIG['tts_K']
    n = len(Y)
    model = fit_model(X[:start_t], Y[:start_t])
    cov, intv = np.zeros(n), np.zeros(n)
    alpha_t = alpha
    buf = list(compute_abs_residuals(model, X[max(0,start_t-50):start_t],
                                     Y[max(0,start_t-50):start_t]))
    for t in range(start_t, n):
        if refit and (t - start_t) % K == 0 and t > start_t:
            s = max(0, t - train_win)
            model = fit_model(X[s:t], Y[s:t])
            cs = max(0, t - 50)
            buf = list(compute_abs_residuals(model, X[cs:t], Y[cs:t]))
        pred = model.predict(X[t:t+1])[0]
        eff_alpha = np.clip(alpha_t, 0.01, 0.99)
        q = np.quantile(buf[-cal_window:], 1 - eff_alpha) if len(buf) > 0 else 1.0
        lo, hi = pred-q, pred+q
        covered = float(lo <= Y[t] <= hi)
        cov[t] = covered; intv[t] = hi - lo
        alpha_t = np.clip(alpha_t + gamma*(alpha - (1.0-covered)), 0.0, 1.0)
        buf.append(abs(Y[t] - pred))
    return cov, intv

def run_dtaci(X, Y, start_t, alpha=0.1, cal_window=200, refit=False, K=50, train_win=200):
    n = len(Y)
    model = fit_model(X[:start_t], Y[:start_t])
    gammas = CONFIG['dtaci_gammas']
    ne = len(gammas)
    alphas = np.full(ne, alpha)
    ew = np.ones(ne) / ne; eta = 0.1
    cov, intv = np.zeros(n), np.zeros(n)
    buf = list(compute_abs_residuals(model, X[max(0,start_t-50):start_t],
                                     Y[max(0,start_t-50):start_t]))
    for t in range(start_t, n):
        if refit and (t - start_t) % K == 0 and t > start_t:
            s = max(0, t - train_win)
            model = fit_model(X[s:t], Y[s:t])
            buf = list(compute_abs_residuals(model, X[max(0,t-50):t], Y[max(0,t-50):t]))
        pred = model.predict(X[t:t+1])[0]
        agg_alpha = np.clip(np.dot(ew, alphas), 0.01, 0.99)
        q = np.quantile(buf[-cal_window:], 1 - agg_alpha) if len(buf) > 0 else 1.0
        lo, hi = pred-q, pred+q
        covered = float(lo <= Y[t] <= hi)
        cov[t] = covered; intv[t] = hi - lo
        err_t = 1.0 - covered
        losses = np.zeros(ne)
        for j in range(ne):
            alphas[j] = np.clip(alphas[j] + gammas[j]*(alpha - err_t), 0.0, 1.0)
            losses[j] = max(alpha*(1-covered), (1-alpha)*covered)
        ew *= np.exp(-eta * losses); ew /= ew.sum()
        buf.append(abs(Y[t] - pred))
    return cov, intv

