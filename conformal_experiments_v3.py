def run_shift_triggered_v3(X, Y, shift1, shift2, tau, alpha=0.1,
                           warmstart=30, cooldown_buffer=20, train_win=200):
    """
    v3 CUSUM with cooldown fix: CUSUM frozen during warmstart + buffer
    to prevent cascading resets. This was the critical v2 bug.
    """
    n = len(Y)
    train_end = 200
    model = fit_model(X[:train_end], Y[:train_end])
    cov, intv = np.zeros(n), np.zeros(n)
    cal_buf = list(compute_abs_residuals(model, X[100:train_end], Y[100:train_end]))
    cusum_val = 0.0
    in_cooldown = False
    cooldown_remaining = 0
    resets = []
    cusum_trace = np.zeros(n)

    for t in range(train_end, n):
        pred = model.predict(X[t:t+1])[0]
        if in_cooldown:
            q = np.quantile(cal_buf, 1-alpha) * 2.0 if len(cal_buf) > 5 else 5.0
            cooldown_remaining -= 1
            if cooldown_remaining <= 0:
                in_cooldown = False
                cusum_val = 0.0
        else:
            q = np.quantile(cal_buf[-200:], 1-alpha) if len(cal_buf) > 5 else 5.0
        lo, hi = pred-q, pred+q
        covered = float(lo <= Y[t] <= hi)
        cov[t] = covered; intv[t] = hi - lo
        cal_buf.append(abs(Y[t] - pred))

        if not in_cooldown:
            err = 1.0 - covered
            cusum_val = max(0, cusum_val + (err - alpha) - 0.2)
            cusum_trace[t] = cusum_val
            if cusum_val > tau:
                resets.append(t)
                cal_buf = []
                in_cooldown = True
                cooldown_remaining = warmstart + cooldown_buffer
                cusum_val = 0.0
                s = max(0, t - train_win)
                model = fit_model(X[s:t], Y[s:t])
        else:
            cusum_trace[t] = 0.0

    return cov, intv, resets, cusum_trace

