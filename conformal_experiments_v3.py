def generate_shifted_ts(n=2000, shift_at=1000, seed=42):
    rng = np.random.RandomState(seed)
    t = np.arange(n)
    X = np.column_stack([np.sin(0.05*t), np.cos(0.05*t), t/n])
    noise = rng.normal(0, 0.5, n)
    Y = np.where(t < shift_at, 2*X[:,0] + noise, -2*X[:,0] + 3*X[:,1] + noise)
    return X, Y, shift_at

def generate_regime_switching(n=2000, shift1=1000, shift2=1500, seed=42):
    rng = np.random.RandomState(seed)
    t = np.arange(n)
    X = np.column_stack([np.sin(0.05*t), np.cos(0.05*t), t/n])
    noise = rng.normal(0, 0.5, n)
    Y = np.zeros(n)
    Y[t < shift1] = 2*X[t < shift1, 0] + noise[t < shift1]
    m2 = (t >= shift1) & (t < shift2)
    Y[m2] = -2*X[m2, 0] + 3*X[m2, 1] + noise[m2]
    Y[t >= shift2] = 2*X[t >= shift2, 0] + noise[t >= shift2]
    return X, Y, shift1, shift2

def generate_hard_drift(n=3000, seed=42):
    rng = np.random.RandomState(seed)
    t = np.arange(n)
    X1 = np.sin(0.01*t) + 0.5*(t/n) + rng.normal(0, 0.3, n)
    X2 = np.cos(0.01*t) - 0.3*(t/n) + rng.normal(0, 0.3, n)
    X3 = rng.normal(0, 0.5, n)
    X = np.column_stack([X1, X2, X3, t/n])
    theta = np.pi * t / (4*n)
    noise = rng.normal(0, 1, n) * (0.4 + 0.3*(t/n))
    Y = 2*np.cos(theta)*X1 + 2*np.sin(theta)*X2 + noise
    return X, Y

def generate_real_data_proxy_electricity(n=3000, seed=42):
    rng = np.random.RandomState(seed)
    t = np.arange(n)
    daily = 3*np.sin(2*np.pi*t/24 - np.pi/4)
    weekly = 1.5*np.sin(2*np.pi*t/168)
    seasonal = np.zeros(n)
    seasonal[t < 1000] = 0.0
    seasonal[(t >= 1000) & (t < 2000)] = 2.5
    seasonal[t >= 2000] = -1.0
    trend = 0.3 * t / n
    hour, dow = t % 24, (t // 24) % 7
    X = np.column_stack([np.sin(2*np.pi*hour/24), np.cos(2*np.pi*hour/24),
        np.sin(2*np.pi*dow/7), np.cos(2*np.pi*dow/7), trend * np.ones(n)])
    Y = daily + weekly + seasonal + trend + rng.normal(0, 0.8, n)
    Y[rng.choice(n, size=20, replace=False)] += rng.uniform(5, 10, 20)
    return X, Y, [1000, 2000]

def generate_real_data_proxy_airquality(n=3000, seed=42):
    rng = np.random.RandomState(seed)
    t = np.arange(n)
    temp = 15 + 10*np.sin(2*np.pi*t/(365*24/6)) + rng.normal(0, 2, n)
    humidity = 60 + 15*np.cos(2*np.pi*t/(365*24/6)) + rng.normal(0, 5, n)
    wind = np.abs(3 + rng.normal(0, 1.5, n))
    pressure = 1013 + 5*np.sin(2*np.pi*t/(180*24/6)) + rng.normal(0, 2, n)
    X = np.column_stack([temp, humidity, wind, pressure, t/n])
    base = 80 + 0.5*humidity - 3*wind - 0.3*temp
    heating = np.zeros(n)
    for cycle in range(n // 500 + 1):
        start = cycle * 500 + 350
        if start < n: heating[start:min(start+150, n)] = 40
    Y = base + heating + rng.exponential(10, n)
    Y = np.clip(Y, 0, None)
    shifts = [s for s in range(350, n, 500) if s < n]
    return X, Y, shifts

