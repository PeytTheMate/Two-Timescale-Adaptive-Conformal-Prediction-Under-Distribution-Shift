def decompose_regret(X, Y, start_t, cov_method, refit=True, K=50, train_win=200):
    n = len(Y)
    oracle = fit_model(X, Y)
    oracle_res = compute_abs_residuals(oracle, X[start_t:], Y[start_t:])
    model_bias = np.zeros(n - start_t)
    cm = fit_model(X[:start_t], Y[:start_t])
    for i, t in enumerate(range(start_t, n)):
        if refit and i % K == 0 and i > 0:
            cm = fit_model(X[max(0,t-train_win):t], Y[max(0,t-train_win):t])
        model_bias[i] = abs(Y[t] - cm.predict(X[t:t+1])[0]) - oracle_res[i]
    cal_lag = np.abs(cov_method[start_t:] - CONFIG['target_coverage'])
    return {'model_bias': rolling_mean(model_bias, 100), 'calibration_lag': rolling_mean(cal_lag, 100)}

