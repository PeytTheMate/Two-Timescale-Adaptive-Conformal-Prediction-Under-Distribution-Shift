    n_pareto = 0
    for ki, K in enumerate(Ks):
        row = f"  K={K:<7}"
        for gi, gamma in enumerate(gammas):
            d, w = results_grid[ki, gi, 0], results_grid[ki, gi, 1]
            star = " *" if (d <= aci_d * 1.3 and w < aci_w * 0.5) else "  "
            if star.strip(): n_pareto += 1
            row += f" {d:>5.1f}/{w:>4.2f}{star}"
